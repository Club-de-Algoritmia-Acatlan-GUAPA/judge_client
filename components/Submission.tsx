import { fetchSubmissions } from '@utils/fetchers'
import React, { CSSProperties, useEffect, useState } from 'react'
import { GetSubmissionsJson } from '@bindings/GetSubmissionsJson'
const Submission = ({ problem_id }: { problem_id: number }) => {
  const [data, setData] = useState<GetSubmissionsJson[]>([])
  useEffect(() => {
    ;(async () => {
      let data = await fetchSubmissions(problem_id)
      if (data.err) {
        setData(dummy3)
      }

      let res: GetSubmissionsJson[] = data.ok
      if (res && Object.keys(res).length === 0) {
        setData(dummy3)
      }

      if (res) {
        setData(res)
        return
      }
      setData(dummy3)
    })()
  }, [])
  const styleBox = {
    display: 'flex',
    background: 'transparent',
    border: 'var(--border-style)',
    width: '100%',
    flexDirection: 'column',
    borderRadius: '10px',
    padding: '10px',
  } as React.CSSProperties
  const flex = {
    display: 'flex',
    flexDirection: 'column',
    color: 'var(--font-primary-color)',
  } as React.CSSProperties
  let utf8decoder = new TextDecoder()
  const thStyle = {
    fontSize: '18px',
    fontWeight: '700',
    fontFamily: 'inter',
    color: 'var(--font-tertiary-color)',
  } as React.CSSProperties

  const casesStyle = {
    display: 'flex',
    flexDirection: 'column',
  } as React.CSSProperties
  const tableStyle = {
    tableLayout: 'fixed',
    width: 'inherit',
  } as React.CSSProperties
  return (
    <>
      <table style={tableStyle}>
        <thead>
          <tr>
            <th style={thStyle}> Submission ID</th>
            <th style={thStyle}> Status</th>
            <th style={thStyle}> Language </th>
            <th style={thStyle}> Submitted at </th>
            {/*<th style={thStyle}> Output </th>*/}
          </tr>
        </thead>
        <tbody>
          {data ? (
            data.map((elem) => {
              //let prepare_output = new elem.prepare_output.
              let prepare_output =
                (elem.output && elem.output.prepare_output?.stderr) || ''
              let date = elem.submitted_at ? (
                <>
                  {new Date(Number(elem.submitted_at)).toLocaleString()}
                  <sup>UTC{new Date().getTimezoneOffset() / -60}</sup>
                </>
              ) : (
                ''
              )
              return (
                <>
                  <tr key={elem.submission_id}>
                    {/*<td> {elem.submission_id.substring(0, 10) + '...'} </td>*/}
                    <td
                      style={{
                        overflow: 'scroll',
                        color: 'var(--blue-primary)',
                        fontWeight: 700,
                      }}
                    >
                      {' '}
                      <a>{elem.submission_id} </a>
                    </td>
                    <td
                      style={{
                        fontWeight: '700',
                        color: GetColor(elem.status),
                      }}
                    >
                      {' '}
                      {elem.status}{' '}
                    </td>
                    <td style={{ fontWeight: 700 }}> {elem.language} </td>
                    <td> {date} </td>
                  </tr>
                  <tr>
                    <td colSpan={4}>
                      <pre style={{ overflow: 'scroll' }}>
                        {' '}
                        {utf8decoder.decode(
                          new Uint8Array(prepare_output),
                        )}{' '}
                      </pre>
                      <div style={casesStyle}>
                        {elem.output &&
                          elem.output.test_cases_results.map((test: any) => {
                            return (
                              <>
                                <div style={flex}>
                                  <div> {test.id}</div>
                                  <pre style={{ overflow: 'scroll' }}>
                                    {' '}
                                    {utf8decoder.decode(
                                      new Uint8Array(test.output?.stdout),
                                    )}
                                  </pre>
                                  <pre style={{ overflow: 'scroll' }}>
                                    {' '}
                                    {utf8decoder.decode(
                                      new Uint8Array(test.output?.stderr),
                                    )}
                                  </pre>
                                </div>
                              </>
                            )
                          })}
                      </div>
                    </td>
                  </tr>
                </>
              )
            })
          ) : (
            <> Loading </>
          )}
        </tbody>
      </table>
    </>
  )
}
