const GetColor = (status: string) => {
  switch (status) {
    case 'Accepted':
      return 'var(--accepted)'
    case 'Runtime Error':
    case 'Wrong Answer':
      return 'var(--wrong-answer)'
    case 'Time Limit Exceeded':
    case 'Compilation Error':
      return 'var(--warning)'
    default:
      return 'var(--font-primary-color)'
  }
}

